	public LevelScreen(ThomasMain g) {
		this.game = g;
		this.camera = new OrthographicCamera();
		camera.setToOrtho(false, 256, 256);
		this.batch = this.game.batch;
		this.shapeRenderer = new ShapeRenderer();
		this.font = this.game.font; // Carica il font di default
		// Inizializza la camera per l'HUD
		hudCamera = new OrthographicCamera();
		hudCamera.setToOrtho(false, 256, 256); // Stessa risoluzione logica, ma fissa
		Pixmap pixmap = new Pixmap(1, 1, Pixmap.Format.RGBA8888);// Crea una texture 1x1 pixel bianca programmata
		pixmap.setColor(Color.WHITE);
		pixmap.fill();
		whitePixel = new Texture(pixmap);
		pixmap.dispose();
	}
	
	@Override
	public void render(float delta) {
		float frameTime = Math.min(delta, 1/60f); 
		 // 2. Pulizia schermo
	    ScreenUtils.clear(0, 0, 0, 1);
		update(frameTime);
		draw(frameTime);
	}
	
	
	public void update(float dt) {
		if (introActive) {
			this.handleIntro(dt);
			// ensure we reset the transition flag while intro is running
			introTransitionHandled = false;
			return; // Salta il resto dell'update (nemici, timer di gioco, ecc.)
		}
		// If we just exited the intro, perform a one-time sync to respawnX/camera to avoid
		// subordinate logic moving player back to levelBeginX unexpectedly
		if (!introTransitionHandled && this.levelInfo != null) {
			this.player.position.x = this.levelInfo.getRespawnX();
			this.player.currentState = Player.State.IDLE;
			this.player.autoWalking = false;
			// orientamento coerente per il piano
			this.player.facingRight = (this.levelInfo.getFloor() == 2 || this.levelInfo.getFloor() == 4) ? true : false;
			this.camera.position.x = this.player.position.x;
			this.camera.update();
			introTransitionHandled = true;
//			if (GameControlRes.debugMode) {
//				System.out.println("[IntroSync] forced PlayerX=" + this.player.position.x + " CameraX=" + this.camera.position.x + " Floor=" + this.levelInfo.getFloor());
//			}
		}

		// Debug: quando l'intro è finita, stampiamo la posizione del player e della camera per diagnosticare teletrasporti
//		if (GameControlRes.debugMode) {
//			String bossState = (boss != null) ? ("active=" + boss.isActive() + ", state=" + boss.getState()) : "no-boss";
//			System.out.println("[Update] PlayerX=" + player.position.x + " CameraX=" + camera.position.x + " " + bossState);
//		}
		// 1. GESTIONE TEMPO DI GIOCO
		GameControlRes.gameTime -= GameControlRes.TIME_SPEED * dt;
		if (GameControlRes.gameTime <= 0) {
			GameControlRes.gameTime = 0;
			if (player.currentState != Player.State.DEAD) {
				player.triggerDeath();
			}
		}
		// 2. UPDATE DEL GIOCATORE
		player.update(dt);
		// 3. GESTIONE LIBERAZIONE (Se Thomas si libera, tutti i Gripper che lo tengono
		// muoiono)
		if (player.currentState != State.DEAD && player.checkAndResetLiberation()) {
			this.handleGrabbingEnemies(dt);
		}

		this.handleEnemies(dt);
		//this.enemyManager.update(dt, player);
		this.handleKnives(dt);
		this.handlePotProjectiles(dt); // NEW: update pot fragments and collisions
		this.handleBoss(dt);
		// Ensure level transition logic runs every frame (some levels rely on boss state or other checks)
		this.startLevelTransition(dt);
		// Debug: print transition-related state when debugMode is on
//		if (GameControlRes.debugMode) {
//			String bossState = (boss != null) ? ("active=" + boss.isActive() + ", state=" + boss.getState()) : "no-boss";
//			System.out.println("[LevelScreen.update] PlayerX=" + player.position.x + " PlayerY=" + player.position.y + " CameraX=" + camera.position.x + " isLevelComplete=" + isLevelComplete + " transitionTimer=" + transitionTimer + " goalX=" + (levelInfo!=null?levelInfo.getGoalX():"null") + " " + bossState);
//		}
		// 6. CAMERA E SPAWN
		updateCamera();
		handleSpawning(dt);
	}
	
	protected abstract void handleIntro(float dt);
	protected abstract void handleGrabbingEnemies(float dt);
	protected abstract void handleEnemies(float dt);
	protected abstract void handleKnives(float dt);
	// --- PotProjectile handling ---
	protected void handlePotProjectiles(float dt) {
		for (int i = potProjectiles.size - 1; i >= 0; i--) {
			PotProjectile p = potProjectiles.get(i);
			p.update(dt);
			if (p.active && p.hitbox.overlaps(player.hurtbox) && player.currentState != Player.State.DEAD) {
				// Danno esempio: 10
				player.takeHit(10f, this, p.position.x, p.position.y);
				p.active = false;
			}
			if (!p.active) potProjectiles.removeIndex(i);
		}
	}
	protected abstract void handleBoss(float dt);
	protected abstract void handleSpawning(float dt);
	public abstract boolean isExceedingBoss(float x);

